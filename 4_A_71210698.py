#daftar harga
IkanBakar = 25000
EsDoger = 6000
RujakCingur = 8000

#proges
print("=====MASUKKAN JUMLAH YANG DIPESAN=====")
MakananI = int(input("Ikan Bakar  Rp 25.000,00 : "))
MakananE = int(input("Es Doger    Rp 6.000,00  : "))
MakananR = int(input("Rujak Cingur Rp 8.000,00  : "))

#Total Harga Makanan
I = IkanBakar*MakananI
E = EsDoger*MakananE
R = RujakCingur*MakananR

print("=====TOTAL=====")
print("TOTAL IKAN BAKAR    : Rp ",I)
print("TOTAL ES DOGER      : Rp ",E)
print("TOTAL RUJAK CINGUR  : Rp ",R)

#Total
total = I+E+R
print("Jumlah total biaya yang harus dibayar adalah Rp",total)


